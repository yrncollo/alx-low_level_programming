/**
 * add - add 2 numbers
 * @a: no 1
 * @b: no 2
 *
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - minus 2 numbers
 * @a: no 1
 * @b: no 2
 *
 * Return: difference
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply 2 numbers
 * @a: no 1
 * @b: no 2
 *
 * Return: multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide 2 numbers
 * @a: no 1
 * @b: no 2
 *
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus of 2 nos
 * @a: no 1
 * @b: no 2
 *
 * Return: modulus
 */
int mod(int a, int b)
{
	return (a % b);
}
